package sw;

public class Planeta {
	private String nome;
	private String regiao;
	
	private Afiliacao planeta;

	public String getNome() {
		return nome;
	}

	public void setNome(String nome) {
		this.nome = nome;
	}

	public String getRegiao() {
		return regiao;
	}

	public void setRegiao(String regiao) {
		this.regiao = regiao;
	}

	public Afiliacao getPlaneta() {
		return planeta;
	}

	public void setPlaneta(Afiliacao planeta) {
		this.planeta = planeta;
	}
	
	
}
